function calc(number1, operator, number2)  {
    if (operator == '+') {
        alert(number1 + number2)
    }
    if (operator == '-') {
        alert(number1 - number2)
    }
    if (operator == '*') {
        alert(number1 * number2)
    }
    if (operator == '/') {
        alert(number1 / number2)
    }
}

calc(13, '*', 23)